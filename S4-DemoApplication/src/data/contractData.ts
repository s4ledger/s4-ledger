  /** Required revision */
  requiredRevision: string
  /** Submittal method */
  submittalMethod: 'IDE' | 'DRL Package' | 'Electronic (EDMS)' | 'IDE + Hard Copy'
  /** Frequency of submission */
  frequency: 'ONE/R' | 'Monthly' | 'Quarterly' | 'As Required' | 'Per Event'
  /** Contract due date */
